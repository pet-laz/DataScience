import numpy as np
def remove_duplicates(arr1, arr2):
    seen = set()
    new_arr1 = []
    new_arr2 = []
    
    for i in range(len(arr1)):
        if arr1[i] not in seen:
            seen.add(arr1[i])
            new_arr1.append(arr1[i])
            new_arr2.append(arr2[i])
    
    return new_arr1, new_arr2

